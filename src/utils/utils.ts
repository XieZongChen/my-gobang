/**
 * 对调棋子颜色
 * @param theme 当前颜色
 * @returns 对调后的颜色
 */
export function reversePiece(theme: PieceTheme) {
  return theme === PieceTheme.black ? PieceTheme.white : PieceTheme.black;
}
